#include <stdlib.h>
#include "libft.h"
#include <string.h>

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*res;

	len = ft_strlen(s1);
	res = (char *)malloc((len + 1) * sizeof(char));
	if (res == NULL)
		return (NULL);
	return (ft_strcpy(res, s1));
}
